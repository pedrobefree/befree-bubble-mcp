def legacy_tool_schema(name: str) -> dict[str, Any]:
    return {
        "name": name,
        "description": (
            "Bubble MCP tool ported from Aria. Accepts the original tool arguments. "
            "Pass profile/app_id/execute/compile/context_file for standalone execution, "
            "or pass write_payload to execute an exact Bubble editor payload."
        ),
        "inputSchema": {
            "type": "object",
            "properties": {},
            "additionalProperties": True,
        },
    }
